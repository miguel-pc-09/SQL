--SELECT -> Sentencia de Seleccion --
--Selecciona todos los registros de la tabla--

SELECT * FROM users; ---> Obtiene todos los registros de la tabla "users"--

--Selecciona 1 parte de la tabla --
SELECT name FROM user;  ---> Obtiene solo la columna nombre  de la tabla "users"--

--Selecciona 2 partes de la tabla -- 
SELECT user_id, name FROM user; --> Obtiene todos los identificadores y nombres de la tabla"users" Separamos con una coma , --


